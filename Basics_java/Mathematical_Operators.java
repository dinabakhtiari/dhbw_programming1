public class Mathematical_Operators {
    public static void main(String[] args) {
        // + - * / %
        int result = 10 + 6;

        // Prefix increment
        //++result;
        result += 3;
        // += -= /= *=
        System.out.println("Result: " + result);


        double result2 = (double)20 / (double)6;
        System.out.println("Result2: " + result2);
    }
}
